let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'search')) {
        el.placeholder = translations[lang][key];
      } else if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit')) {
        el.value = translations[lang][key];
      } else if (el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  document.getElementById('language-selector').value = lang;
}

async function loadSymptomsData() {
  const response = await fetch('data/symptoms_cures.json');
  const data = await response.json();
  return data.diseases;
}

function findDiseaseBySymptoms(inputSymptoms, diseases) {
  const inputSet = new Set(inputSymptoms.map(s => s.toLowerCase().trim()));
  let bestMatch = null;
  let maxMatches = 0;

  for (const disease of diseases) {
    let diseaseSymptoms = [];
    if (typeof disease.symptoms === 'object') {
      diseaseSymptoms = Object.values(disease.symptoms).flat();
    } else {
      diseaseSymptoms = disease.symptoms || [];
    }
    const diseaseSymptomsLower = diseaseSymptoms.map(s => s.toLowerCase());
    const matches = diseaseSymptomsLower.filter(symptom => inputSet.has(symptom)).length;
    if (matches > maxMatches) {
      maxMatches = matches;
      bestMatch = disease;
    }
  }
  return bestMatch;
}

